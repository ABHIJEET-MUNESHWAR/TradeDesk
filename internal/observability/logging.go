package observability

import (
	"log/slog"
	"os"
)

// NewLogger builds a structured JSON logger at the given level. JSON output is
// the ingestion format for Loki/ELK and keeps correlation ids queryable.
func NewLogger(level string) *slog.Logger {
	var lvl slog.Level
	switch level {
	case "debug":
		lvl = slog.LevelDebug
	case "warn":
		lvl = slog.LevelWarn
	case "error":
		lvl = slog.LevelError
	default:
		lvl = slog.LevelInfo
	}
	handler := slog.NewJSONHandler(os.Stdout, &slog.HandlerOptions{Level: lvl})
	return slog.New(handler)
}
